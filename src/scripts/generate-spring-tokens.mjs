#!/usr/bin/env node
/**
 * Generate CSS linear() spring easing tokens from motion.ts spring configs.
 *
 * Uses the spring-easing package (devDependency) to produce CSS linear()
 * approximations of spring physics curves. Output is designed to be pasted
 * into tokens.css.
 *
 * Usage:
 *   node scripts/generate-spring-tokens.mjs
 *
 * The spring parameters match src/lib/motion.ts:
 *   SPRING_GENTLE:  { stiffness: 120, damping: 14, mass: 1 }
 *   SPRING_SNAPPY:  { stiffness: 300, damping: 20, mass: 1 }
 *   SPRING_BOUNCY:  { stiffness: 180, damping: 12, mass: 1 }
 *
 * spring() param order: spring(mass, stiffness, damping, velocity)
 */

import { SpringEasing, CSSSpringEasing } from 'spring-easing';

const springs = [
  { name: 'gentle',  mass: 1, stiffness: 120, damping: 14 },
  { name: 'snappy',  mass: 1, stiffness: 300, damping: 20 },
  { name: 'bouncy',  mass: 1, stiffness: 180, damping: 12 },
];

const NUM_POINTS = 64;
const DECIMAL = 3;

console.log('/* --- Spring Easing Tokens (generated from motion.ts configs) ----');
console.log(' * True spring overshoot via CSS linear(). Generated by:');
console.log(' *   node scripts/generate-spring-tokens.mjs');
console.log(' * Keep --ease-spring cubic-bezier above as legacy fallback.');
console.log(' */');

for (const spring of springs) {
  const easingStr = `spring(${spring.mass}, ${spring.stiffness}, ${spring.damping}, 0)`;

  // Get raw frames for full-fidelity linear() output
  const [frames] = SpringEasing([0, 1], {
    easing: easingStr,
    numPoints: NUM_POINTS,
    decimal: DECIMAL,
  });

  // Build linear() from raw frames (no lossy simplification)
  const linearValue = `linear(${frames.join(', ')})`;

  // Get duration from the CSS variant (it computes the correct spring duration)
  const [, duration] = CSSSpringEasing({
    easing: easingStr,
    numPoints: NUM_POINTS,
    decimal: DECIMAL,
  });

  console.log(`--ease-spring-${spring.name}: ${linearValue};`);
  console.log(`--duration-spring-${spring.name}: ${Math.round(duration)}ms;`);
}
